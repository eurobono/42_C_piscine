#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	c;
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			c = str[i] + 32;
			str[i] = c;
		}
		i++;
	}
	return (str);
}
